#include <iostream>

int main(int argc, char **argv){
	int _integer = 15; //Decimal
	int _octal = 017;
	int _headecimal = 0x0F;
	int _binary = 0b00001111;

	std::cout << "Integer: " << _integer << std::endl;
	std::cout << "Octal: " << _octal  << std::endl;
	std::cout << "Hexadecimal: " << _headecimal  << std::endl;
	std::cout << "Binary: " << _binary  << std::endl;





	return 0;
}
